function inf(arr){
    let result = "";
    let div = arr.nodeName;
    let text = arr.textContent;
    let id = arr.id;
    let clas = arr.className;
    if ( id == "colocol"){
        result += "Динь-динь\n";
    }
    result += "Элемент: <" + div + ">\n" ;
    result += "Атрибуты: id=" + id  + "\n" ;
    if ( clas != ""){
        result += "Атрибуты: class=" + clas  + "\n" ;
    }
    if ( text != ""){
        result += "Текст: " + text;
    }
    return result;
}